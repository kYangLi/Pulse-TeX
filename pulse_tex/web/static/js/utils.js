function escapeHtml(text) {
    if (!text) return '';
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML;
}

function formatDate(dateStr) {
    if (!dateStr) return '';
    const date = new Date(dateStr);
    const now = new Date();
    const diff = Math.floor((now - date) / 1000);
    
    if (diff < 60) return t('common.justNow') || 'Just now';
    if (diff < 3600) return `${Math.floor(diff / 60)} ${t('common.minutesAgo') || 'min ago'}`;
    if (diff < 86400) return `${Math.floor(diff / 3600)} ${t('common.hoursAgo') || 'hours ago'}`;
    if (diff < 604800) return `${Math.floor(diff / 86400)} ${t('common.daysAgo') || 'days ago'}`;
    
    return date.toLocaleDateString();
}

function formatTimeAgo(date) {
    if (!date) return 'never';
    const now = new Date();
    const diff = Math.floor((now - date) / 1000);
    
    if (diff < 60) return t('common.justNow') || 'Just now';
    if (diff < 3600) return `${Math.floor(diff / 60)} ${t('common.minutesAgo') || 'min ago'}`;
    if (diff < 86400) return `${Math.floor(diff / 3600)} ${t('common.hoursAgo') || 'hours ago'}`;
    return date.toLocaleTimeString();
}

function debounce(func, wait) {
    let timeout;
    return function executedFunction(...args) {
        const later = () => {
            clearTimeout(timeout);
            func(...args);
        };
        clearTimeout(timeout);
        timeout = setTimeout(later, wait);
    };
}

function throttle(func, limit) {
    let inThrottle;
    return function(...args) {
        if (!inThrottle) {
            func.apply(this, args);
            inThrottle = true;
            setTimeout(() => inThrottle = false, limit);
        }
    };
}

function downloadFile(url, filename) {
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
}

function copyToClipboard(text) {
    return navigator.clipboard.writeText(text);
}

